
# Write a Python program to concatenate two tuples into one.

# Define two tuples
tuple1 = (1, 2, 3)
tuple2 = ("a", "b", "c")

# Concatenate the tuples
combined_tuple = tuple1 + tuple2

# Display the results
print("First Tuple:", tuple1)
print("Second Tuple:", tuple2)
print("Concatenated Tuple:", combined_tuple)

