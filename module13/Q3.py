
# Write a Python program to demonstrate the creation of variables and different data types

# This program demonstrates the creation of variables
# with different data types in Python

# Integer
age = 25

# Float
height = 5.9

# String
name = "Harsh Patel"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10.5, 20.3)

# Set
unique_numbers = {1, 2, 3, 3}

# Dictionary
person = {
    "name": "Harsh",
    "age": 25,
    "city": "Ahmedabad"
}

# Display all variables and their types
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)
print("Fruits:", fruits)
print("Coordinates:", coordinates)
print("Unique Numbers:", unique_numbers)
print("Person Info:", person)

