
# Write a Python program to demonstrate the use of functions from
# the math module..

import math 

num = 15.25

print("Using math module funstion with number:", num)

print("square root:", math.sqrt(num))
print("ceilling value:", math.ceil(num))
print("floor value:", math.floor(num))
print("power (num^2):", math.pow(num, 2))
print("Natural log (ln):", math.log10(num))