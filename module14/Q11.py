
# Write a Python program to access values between index 1 and 5 in a tuple. 1

# Define a tuple
my_tuple = ("apple", "banana", "cherry", "date", "grape", "kiwi", "mango")

# Access elements from index 1 to 4 (5 is excluded)
sliced_tuple = my_tuple[1:5]

# Display the results
print("Original Tuple:", my_tuple)
print("Elements from index 1 to 4:", sliced_tuple)
